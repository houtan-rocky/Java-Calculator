package math;

public class Calculator {
    static public int sum(int firstNumber, int secondNumber) {
        return firstNumber + secondNumber;
    }
    static public float sum(float firstNumber, float secondNumber) {
        return firstNumber + secondNumber;
    }

    static public int subtract(int firstNumber, int secondNumber) {
        return firstNumber - secondNumber;
    }

    static public float subtract(float firstNumber, float secondNumber) {
        return firstNumber - secondNumber;
    }

    static public int multiply(int firstNumber, int secondNumber) {
        return firstNumber * secondNumber;
    }
    static public float multiply(float firstNumber, float secondNumber) {
        return firstNumber * secondNumber;
    }

    static public int divide(int firstNumber, int secondNumber) {
        return firstNumber / secondNumber;
    }
    static public float divide(float firstNumber, float secondNumber) {
        return firstNumber / secondNumber;
    }



}
